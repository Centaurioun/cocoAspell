#import "Dictionary.h"


@implementation Dictionary

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (id)init
{
	self	= [super init];
	if (self) {
		mEnabled	= NO;
	}
	return self;
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (NSString*)displayName
{
	return @"";
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (NSString*)identifier
{
	return @"";
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (NSString*)appleName
{
	return @"";
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (NSString*)info
{
	return @"";
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (BOOL)isEnabled
{
	return mEnabled;
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (void)setEnabled:(BOOL)enabled
{
	mEnabled	= enabled;
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (BOOL)isEqual:(id)anObject
{
	if (self == anObject)
		return YES;
	return [[self identifier] isEqual:[anObject identifier]];
}
 
// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (NSComparisonResult)compareDisplayNames:(Dictionary*)desc
{
	return [[self displayName] compare:[desc displayName]];
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (long)canCompile
{
	return 0;
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (BOOL)isCompiled
{
	return YES;
}

// ----------------------------------------------------------------------------
// 
// ----------------------------------------------------------------------------

- (BOOL)compileWithBundle:(NSBundle*)appBundle andProgressCallback:(id<CompileProgress>)callback
{
	return YES;
}


@end
